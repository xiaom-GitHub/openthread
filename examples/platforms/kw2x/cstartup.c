/**
 * __low_level_init is the first instructions that are run on boot.
 */

#include "platform-kw2x.h"

#define WDOG_UNLOCK1    0xC520U
#define WDOG_UNLOCK2    0xD928U

void __cpu_startup(void)
{
    // Disable watchdog
    WDOG_UNLOCK = WDOG_UNLOCK1;
    WDOG_UNLOCK = WDOG_UNLOCK2;
    WDOG_STCTRLH &= ~(1 << 0);

    // Enable all of the port clocks.  Need them to setup mux configuration.
    SIM_SCGC5 |= (SIM_SCGC5_PORTA_MASK
                  | SIM_SCGC5_PORTB_MASK
                  | SIM_SCGC5_PORTC_MASK
                  | SIM_SCGC5_PORTD_MASK
                  | SIM_SCGC5_PORTE_MASK);
}
